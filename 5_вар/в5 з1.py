
import numpy as np

# Данные: x, y, z, m
# x: 1, 2, 3, 4, 5, 6, 7
# y: 71, 79, 12, 45, 88, 53, 43
# z: 91, 10, 48, 43, 45, 3, 19
# m: 10, 48, 43, 45, 3, 19, 7  (массы, предположим, даны, например, последнее число 7 вместо 0)

x = np.array([1, 2, 3, 4, 5, 6, 7])
y = np.array([71, 79, 12, 45, 88, 53, 43])
z = np.array([91, 10, 48, 43, 45, 3, 19])
m = np.array([10, 48, 43, 45, 3, 19, 7])  # Предположили массы

# Центр масс
M = np.sum(m)
x_c = np.sum(m * x) / M
y_c = np.sum(m * y) / M
z_c = np.sum(m * z) / M

print(f"Центр масс: ({x_c:.2f}, {y_c:.2f}, {z_c:.2f})")

# Расстояния до центра масс
dist = np.sqrt((x - x_c)**2 + (y - y_c)**2 + (z - z_c)**2)
for i in range(len(x)):
    print(f"Точка {i+1}: {dist[i]:.2f}")

# Максимальное расстояние
max_d = np.max(dist)
far_points = np.where(dist == max_d)[0] + 1
print(f"\nМаксимальное расстояние: {max_d:.2f}")
print(f"Точки на макс. расстоянии: {far_points}")
